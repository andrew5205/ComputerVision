"""random"""
np.random.seed(101)
arr = np.random.randint(0, 100, 10)
print(arr)                  # [95 11 81 70 63 87 75  9 77 40]

print(arr.max())            # 95
print(arr.argmax())         # 0

print(arr.min())            # 9
print(arr.argmin())         # 7

print(arr.mean())           # 60.8


"""
re-shape
"""
print(arr.shape)            # (10,)

print(arr.reshape(2,5))
# [[95 11 81 70 63]
#  [87 75  9 77 40]]




